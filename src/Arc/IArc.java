package Arc;
/**
 * The Interface IArc.
 */
public interface IArc {
	
	/**
	 * Fire.
	 */
	public void fire();
	
	/**
	 * Gets the weight.
	 *
	 * @return the weight
	 */
	public int getWeight();
	
	/**
	 * Sets the weight.
	 *
	 * @param weight the new weight
	 */
	public void setWeight(int weight);
}